import turtle
t=turtle.Turtle()
t.pensize(4)
t.begin_fill()
t.fillcolor("red")
t.speed(6)
for i in range(2):
    t.forward(200)
    t.left(90)
t.left(45)   
t.forward(282.85)
t.right(90)
t.forward(282.85)
t.left(135)
t.forward(200)
t.circle(200)
t.left(90)
t.forward(200)
t.end_fill ()
t.goto(0,-200)
t.left(180)
t.begin_fill()
t.fillcolor("red")
t.circle(300)
t.end_fill()
turtle.done() 